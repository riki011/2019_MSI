    private boolean validar() {
        boolean flag = true;
        String mensaje = "";

        if (txtUsuario.getText().isEmpty()) {
            flag = false;
            mensaje += "EL NOMBRE DEL USUARIO NO PUEDE QUEDAR VACIO \n";
        }
        
         if ("admin".equals(txtUsuario.getText())) {
            flag = false;
            mensaje += "EL USUARIO ADMIN YA EXISTE \n";
        }
        
        
        
  
               String pas = String.valueOf(txtpass.getPassword());
        if (pas.isEmpty()) {
            flag = false;
            mensaje += "LA PASSWORD NO PUEDE QUEDAR VACIA! \n";
        } 

       
        if (!flag) {
            JOptionPane.showMessageDialog(null, mensaje);

        }
        return flag;
    }

