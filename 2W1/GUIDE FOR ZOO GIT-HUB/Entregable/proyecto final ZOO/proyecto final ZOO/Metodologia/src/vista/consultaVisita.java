             private void cargarTablaParametrizada(int id) 
    {
       
        
        
        DefaultTableModel dtm = new DefaultTableModel();
        
        listaVisitasMagica= new GestorVisita().listaVisitaParametrizada(id);
        
        dtm.setColumnIdentifiers(new String[]{"numero de visita","institucion","guia","recorrido","representante", "cantidad visitantes","fecha de reserva"});
        
        for (DtoVisitas dt : listaVisitasMagica) 
        {
            
            dtm.addRow(new Object[]{dt.getNroVisita(),dt.getInstitucion(),dt.getGuia(),dt.getRecorrido(),dt.getRepresentante(),dt.getCantVisitantes(),dt.getFechaReserva()});
        }
        
        jtVisita.setModel(dtm);
        
        
    
    }//GEN-LAST:event_btnVolverAlmenuActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        
     if(txtNum.getText().isEmpty())
     {
         JOptionPane.showMessageDialog(null, "ingrese un numero de visita");
     
     }
        
         int numero = Integer.parseInt(txtNum.getText());
         GestorVisita g = new GestorVisita();
         g.listaVisitaParametrizada(numero);
         
         cargarTablaParametrizada(numero);
         
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnBuscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscar1ActionPerformed
        // TODO add your handling code here:
        int num = Integer.parseInt(txtNum.getText());
        
        GestorVisita g = new GestorVisita();
      
       if (JOptionPane.showConfirmDialog(rootPane, "Se eliminará el registro, ¿desea continuar?",
        "Eliminar Registro", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION)
          g.deleteVisita(num);
        
         txtNum.setText("");
        
        
       // JOptionPane.showMessageDialog(null, "VISITA ELIMINADA");
         
        
            cargarTabla();
        
    }//GEN-LAST:event_btnBuscar1ActionPerformed

