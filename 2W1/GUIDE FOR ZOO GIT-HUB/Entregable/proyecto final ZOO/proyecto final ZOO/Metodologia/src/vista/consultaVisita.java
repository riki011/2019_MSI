    private void cargarTabla() 
    {
        
        DefaultTableModel dtm = new DefaultTableModel();
        
        listaVisitasMagica= new GestorVisita().listaVisita();
        
        dtm.setColumnIdentifiers(new String[]{"numero de visita","institucion","guia","recorrido","representante", "cantidad visitantes","fecha de reserva","Horario del Recorrido"});
        
        for (DtoVisitas dt : listaVisitasMagica) 
        {
            
            dtm.addRow(new Object[]{dt.getNroVisita(),dt.getInstitucion(),dt.getGuia(),dt.getRecorrido(),dt.getRepresentante(),dt.getCantVisitantes(),dt.getFechaReserva(),dt.getHoraioRecorrido()});
        }
        
        jtVisita.setModel(dtm);
        
        
    }

